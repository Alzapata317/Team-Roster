class Intern {
    constructor(name, id, email, school, role) {
        this.name = name;
        this.id = id;
        this.email = email;
        this.school = school;
        this.role = role;
    }
    getName() {
        returns: this.name;
    }
    getId() {
        returns: this.id;
    }
    getEmail() {
        returns: this.email;
    }
    getSchool() {
        returns: this.school;
    }
    getRole() {
        returns: this.role;
    }
  }
  
  module.exports = Intern;